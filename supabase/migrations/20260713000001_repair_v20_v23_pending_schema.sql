-- Ver2.1: 国家開発区画の残り列
-- ============================================================

alter table users
  add column if not exists development_area text;

alter table users
  add column if not exists development_plot_status text not null default 'preparing';

do $$
begin
  if not exists (
    select 1 from pg_constraint
    where conname = 'users_development_plot_status_check'
  ) then
    alter table users
      add constraint users_development_plot_status_check
      check (development_plot_status in ('preparing', 'nation_building', 'metaverse_pending', 'priority', 'confirming'));
  end if;
end $$;

