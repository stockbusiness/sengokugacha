-- 修復用マイグレーション: Ver2.0〜Ver2.3で追加したDDLが本番環境に未適用だったため、
-- IF NOT EXISTS を付けた冪等な形で再発行する。既に適用済みの環境でも安全に実行できる。
-- 対象: 20260709000006, 20260710000001, 20260712000001 の各マイグレーション。

-- ============================================================
-- Ver2.0: users テーブル(国民証・創設メンバー/建国メンバーの受け皿)
-- ============================================================

alter table users
  add column if not exists national_number bigint generated by default as identity,
  add column if not exists contribution_points int not null default 0,
  add column if not exists is_founding_member boolean not null default false,
  add column if not exists development_plot_id text,
  add column if not exists founding_member_number int,
  add column if not exists is_nation_builder boolean not null default false,
  add column if not exists nation_builder_plan text,
  add column if not exists nation_builder_joined_at timestamptz;

create unique index if not exists idx_users_founding_member_number on users(founding_member_number)
  where founding_member_number is not null;

create table if not exists daily_mission_completions (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references users(id),
  mission_key text not null,
  completed_date date not null default current_date,
  created_at timestamptz not null default now(),
  unique (user_id, mission_key, completed_date)
);

create index if not exists idx_daily_mission_completions_user_date on daily_mission_completions(user_id, completed_date);

alter table daily_mission_completions enable row level security;

