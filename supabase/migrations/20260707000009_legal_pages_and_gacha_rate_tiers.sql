-- ガチャ排出率(制圧済み国数に応じたレア/中間排出率の階層設定)。
-- 03_gacha_game_design_v1.4.md 9章のティア表に対応する初期値をseedする。
-- tier_order の昇順で評価し、max_conquered_count が null の行を最後の「それ以降すべて」の階層として扱う。
-- ユーザー向け排出率開示(/rates)にもこの値をそのまま表示するため、admin画面での編集がそのまま開示内容に反映される。

create table gacha_rate_tiers (
  id uuid primary key default gen_random_uuid(),
  tier_order int not null unique,
  max_conquered_count int,
  rare_rate numeric(6, 4) not null,
  mid_rate numeric(6, 4) not null,
  updated_at timestamptz not null default now()
);

alter table gacha_rate_tiers enable row level security;

